export enum CARDS
{
    SIX_HEARTH    = 0b00010000,
    SEVEN_HEARTH  = 0b00010001,
    EIGHT_HEARTH  = 0b00010010,
    NINE_HEARTH   = 0b00010011,
    TEN_HEARTH    = 0b00010100,
    JACK_HEARTH   = 0b00010101,
    QUEEN_HEARTH  = 0b00010110,
    KING_HEARTH   = 0b00010111,
    ACE_HEARTH    = 0b00011000,
    SIX_DIAMOND   = 0b00100000,
    SEVEN_DIAMOND = 0b00100001,
    EIGHT_DIAMOND = 0b00100010,
    NINE_DIAMOND  = 0b00100011,
    TEN_DIAMOND   = 0b00100100,
    JACK_DIAMOND  = 0b00100101,
    QUEEN_DIAMOND = 0b00100110,
    KING_DIAMOND  = 0b00100111,
    ACE_DIAMOND   = 0b00101000,
    SIX_CLUBS     = 0b01000000,
    SEVEN_CLUBS   = 0b01000001,
    EIGHT_CLUBS   = 0b01000010,
    NINE_CLUBS    = 0b01000011,
    TEN_CLUBS     = 0b01000100,
    JACK_CLUBS    = 0b01000101,
    QUEEN_CLUBS   = 0b01000110,
    KING_CLUBS    = 0b01000111,
    ACE_CLUBS     = 0b01001000,
    SIX_SPADES    = 0b10000000,
    SEVEN_SPADES  = 0b10000001,
    EIGHT_SPADES  = 0b10000010,
    NINE_SPADES   = 0b10000011,
    TEN_SPADES    = 0b10000100,
    JACK_SPADES   = 0b10000101,
    QUEEN_SPADES  = 0b10000110,
    KING_SPADES   = 0b10000111,
    ACE_SPADES    = 0b10001000
}

export function findCardColor(card: CARDS) {
    return card & 0b11110000;
}

export function findCardValue(card: CARDS) {
    return card & 0b00001111;
}